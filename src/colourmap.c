/*
 * Sets the colour map
 */
int pgwrapSetColourMap(int iCMap, int iIsColInv, float fColMin, float fColMax)
{
    int iColMin = 0;
    int iColMax = 0;
    int iColIndex = 0;
    int i = 0;
    float (*paafCMap)[PGWRAP_CMAP_LEVELS][3]
        = (float (*) [PGWRAP_CMAP_LEVELS][3]) g_pgwrap_aaafCMap[iCMap];

    /* query colour capability */
    cpgqcol(&iColMin, &iColMax);
    if (1 == iColMax)   /* device has no colour capability */
    {
        (void) printf("WARNING: Device has no colour capability\n!");
        return EXIT_SUCCESS;
    }

    for (i = 0; i < 64; ++i)
    {
        iColIndex = i + 2;
        cpgscr(iColIndex,
               (*paafCMap)[i][0],
               (*paafCMap)[i][1],
               (*paafCMap)[i][2]);
    }
    cpgscir(2, iColIndex);

    return EXIT_SUCCESS;
}

